    private void jButtonEkleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEkleActionPerformed
        // TODO add your handling code here:
        String ISBNo=jTextFieldISBNo.getText().trim();
        String kitapAdi=jTextFieldKitapAdi.getText().trim();
        String sayfaSayisi=jTextFieldSayfaSayisi.getText().trim();
        boolean sayfaSayisiKontrol=new Mesajlar().sayisalMi(sayfaSayisi);
        int sayfaSayisiInt=0;
        if(sayfaSayisiKontrol==false){
            JOptionPane.showMessageDialog(null, new Mesajlar().GecerliSayfaSayisiGiriniz());
            return;
        }

        if(sayfaSayisi.length()<=0){
            JOptionPane.showMessageDialog(null, new Mesajlar().SayfaSayisiGiriniz());
            return;
        }else{
            sayfaSayisiInt=Integer.parseInt(sayfaSayisi);
        }

        int kitapTurIndeks=jComboBoxKitapTur.getSelectedIndex();
        if(kitapTurIndeks<=0){
            JOptionPane.showMessageDialog(null, new Mesajlar().KitapTurSeciminiziYapiniz());
            return;
        }

        tr.isparta.edu.DAL.KitapTur kitapTurDeger=kitapTurListe.get(kitapTurIndeks-1);
        //JOptionPane.showMessageDialog(null, kitapTurDeger.getAd()+" "+kitapTurDeger.getID());

        int kitapYazarIndeks=jComboBoxYazarAdi.getSelectedIndex();
        if(kitapYazarIndeks<=0){
            JOptionPane.showMessageDialog(null, new Mesajlar().KitapYazarSeciminiziYapiniz());
            return;
        }

        tr.isparta.edu.DAL.KitapYazar kitapYazarDeger=kitapYazarListe.get(kitapYazarIndeks-1);
        //JOptionPane.showMessageDialog(null, kitapYazarDeger.getAd()+" "+kitapYazarDeger.getSoyad()+" "+kitapYazarDeger.getID());

        KitapService kitap=new KitapService();
        kitap.Ekleme(ISBNo, kitapAdi, sayfaSayisiInt, kitapYazarDeger.getID(), kitapTurDeger.getID());

        Listele();
    }//GEN-LAST:event_jButtonEkleActionPerformed

