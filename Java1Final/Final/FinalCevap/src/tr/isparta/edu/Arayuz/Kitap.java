    private void jTableSonucMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableSonucMouseClicked
        // TODO add your handling code here:

        DefaultTableModel model=(DefaultTableModel)jTableSonuc.getModel();
        int secilen=jTableSonuc.getSelectedRow();

        jTextFieldID.setText(model.getValueAt(secilen,0).toString());
        jTextFieldISBNo.setText(model.getValueAt(secilen,1).toString());
        jTextFieldKitapAdi.setText(model.getValueAt(secilen,2).toString());
        jTextFieldSayfaSayisi.setText(model.getValueAt(secilen, 3).toString());

        int ID=Integer.parseInt(model.getValueAt(secilen,0).toString());
        int[] kitapYazarTur=new KitapService().GetKitapYazarTurByID(ID);

        int kitapYazarIndeks=0, kitapTurIndeks=0;

        for (int i = 0; i < kitapYazarListe.size(); i++) {
            if(kitapYazarListe.get(i).getID()==kitapYazarTur[0])   {
                //JOptionPane.showMessageDialog(null, kitapYazarListe.get(i).getID());
                kitapYazarIndeks=i;
                break;
            }
        }

        for (int i = 0; i < kitapTurListe.size(); i++) {
            if(kitapTurListe.get(i).getID()==kitapYazarTur[1]){
                kitapTurIndeks=i;
                //JOptionPane.showMessageDialog(null, kitapTurListe.get(i).getID());
                break;
            }
        }

        jComboBoxYazarAdi.setSelectedIndex(kitapYazarIndeks+1);
        jComboBoxKitapTur.setSelectedIndex(kitapTurIndeks+1);

    }//GEN-LAST:event_jTableSonucMouseClicked

